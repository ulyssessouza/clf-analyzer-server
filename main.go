package main

import (
	"bufio"
	"fmt"
	"os"

	"github.com/labstack/echo"
	"github.com/labstack/echo/middleware"
	"github.com/swaggo/echo-swagger"

	logparser "github.com/Songmu/axslogparser"

	_ "github.com/ulyssessouza/clf-analyzer-server/docs" // docs is generated by Swag CLI
	"github.com/ulyssessouza/clf-analyzer-server/http"
)

func Ingest() {
	scanner := bufio.NewScanner(os.Stdin)

	for scanner.Scan() {
		line := scanner.Text()

		parser, log, err := logparser.GuessParser(line)
		if err != nil {
			fmt.Errorf("%s\n", err)
		}

		if _, ok := parser.(*logparser.Apache); !ok {
			fmt.Errorf("Couldn't parse log line: %s\n", line)
		}

		fmt.Printf("ReadLine: %s\n", line)
		fmt.Printf("Log: %v\n", log)
	}
}

func main() {
	go Ingest()

	port := 8000 // To be set from command line
	e := echo.New()
	e.HideBanner = true

	e.Use(middleware.Logger())
	e.Use(middleware.Recover())

	e.GET("/", http.RootHandler)
	e.GET("/test", http.TestHandler)
	e.GET("/swagger/*", echoSwagger.WrapHandler)
	e.GET("/hello", http.HelloWS)

	e.Logger.Fatal(e.Start(fmt.Sprintf(":%d", port)))
}
